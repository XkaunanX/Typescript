// Tipos primitivos en TypeScript
let myString: string = "Hola, TypeScript";
let myNumber: number = 42;
let myBoolean: boolean = true;

// Operaciones con cadenas
myString = myString.toUpperCase();
console.log(`String en mayúsculas: ${myString}`);

// Operaciones con números
myNumber = myNumber * 2;
console.log(`Número multiplicado por 2: ${myNumber}`);

// Operaciones con booleanos
if (myBoolean) {
  console.log("El valor booleano es verdadero.");
} else {
  console.log("El valor booleano es falso.");
}
